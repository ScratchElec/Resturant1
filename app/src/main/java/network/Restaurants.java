public class Restaurants {
    private int Restaurant_id;
    private String Restaurant_Logo_URL;
    private String Restaurant_Name;
    private String Restaurant_Location;
    private String Restaurant_Map_Latitude;
    private String Restaurant_Map_Longitude;

    public int getRestaurant_id() {
        return Restaurant_id;
    }

    public void setRestaurant_id(int restaurant_id) {
        Restaurant_id = restaurant_id;
    }

    public String getRestaurant_Logo_URL() {
        return Restaurant_Logo_URL;
    }

    public void setRestaurant_Logo_URL(String restaurant_Logo_URL) {
        Restaurant_Logo_URL = restaurant_Logo_URL;
    }

    public String getRestaurant_Name() {
        return Restaurant_Name;
    }

    public void setRestaurant_Name(String restaurant_Name) {
        Restaurant_Name = restaurant_Name;
    }

    public String getRestaurant_Location() {
        return Restaurant_Location;
    }

    public void setRestaurant_Location(String restaurant_Location) {
        Restaurant_Location = restaurant_Location;
    }

    public String getRestaurant_Map_Latitude() {
        return Restaurant_Map_Latitude;
    }

    public void setRestaurant_Map_Latitude(String restaurant_Map_Latitude) {
        Restaurant_Map_Latitude = restaurant_Map_Latitude;
    }

    public String getRestaurant_Map_Longitude() {
        return Restaurant_Map_Longitude;
    }

    public void setRestaurant_Map_Longitude(String restaurant_Map_Longitude) {
        Restaurant_Map_Longitude = restaurant_Map_Longitude;
    }
}
